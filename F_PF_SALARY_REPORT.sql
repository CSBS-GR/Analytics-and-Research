drop table f_pf_salary_report;
create table f_pf_salary_report as
SELECT * FROM (SELECT 
MAX(ACCOUNT_ID) as account_id,
ACCOUNT,
YEAR,
row_var,
COL_VAR,
N_INCUMBENTS,
SALARY,
ANSWER_CHECKBOX,
STATE,
STATE_ID,
SDI_STATE_ID,
DISTRICT_ID,
TITLE_ID,
SALARY_ID,
ROW_QUESTION_ID,
COL_QUESTION_ID
FROM F_SALARY_REPORT_err A
GROUP BY ACCOUNT,
YEAR,
COL_VAR,
row_var,
N_INCUMBENTS,
SALARY,
ANSWER_CHECKBOX,
STATE,
STATE_ID,
SDI_STATE_ID,
DISTRICT_ID,
TITLE_ID,
SALARY_ID,
ROW_QUESTION_ID,
COL_QUESTION_ID
) A
